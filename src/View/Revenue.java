    private void displayRevenues(List<Model.Revenue> revenues) {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Year");
        model.addColumn("Quarter");
        model.addColumn("Month");
        model.addColumn("Receipt");
        for (Model.Revenue revenue : revenues) {
            model.addRow(new Object[]{revenue.getYear(), revenue.getQuarter(), revenue.getMonth(), revenue.getReceipt()});
        }
        jTable1.setModel(model);
    }
    
    private void displayBooking(List<Model.Booking> booking) {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Schedule Id");
        model.addColumn("Customer Id");
        model.addColumn("Receipt");
        for (Model.Booking book : booking) {
            model.addRow(new Object[]{book.getId(), book.getScheduleId(), book.getCustomerId(), book.getReceipt()});
        }
        jTable1.setModel(model);
    }
    
    private void displaySchedule(List<Model.Schedule> schedules) {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Id");
        model.addColumn("Gate");
        model.addColumn("Departure");
        model.addColumn("Departure Time");
        model.addColumn("Status");
        for (Model.Schedule schedule : schedules) {
            model.addRow(new Object[]{schedule.getId(), schedule.getGate(), schedule.getDeparture(), schedule.getDepartureTime(), schedule.getStatus()});
        }
        jTable1.setModel(model);
    }

