    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFrame frame = new JFrame("Modal Dialog Example");
        // Set the size of the frame
        frame.setSize(300, 200);
        // Set the default close operation
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        
        JPanel panel = new JPanel();
        JRadioButton option1 = new JRadioButton("Month");
        JRadioButton option2 = new JRadioButton("Quarter");
        JRadioButton option3 = new JRadioButton("Year");

        ButtonGroup group = new ButtonGroup();
        group.add(option1);
        group.add(option2);
        group.add(option3);

        panel.add(option1);
        panel.add(option2);
        panel.add(option3);

        int result = JOptionPane.showConfirmDialog(frame, panel, "Choose an Option", JOptionPane.OK_CANCEL_OPTION);
        if (result == JOptionPane.OK_OPTION) {
            if (option1.isSelected()) {
                try {
                    List<Model.Revenue> revenues = dao.revenueByMonth();
                    displayRevenues(revenues);
                }
                catch(Exception e){
                }
            } else if (option2.isSelected()) {
                try {
                    List<Model.Revenue> revenues = dao.revenueByQuarter();
                    displayRevenues(revenues);
                }
                catch(Exception e){
                }
            } else if (option3.isSelected()) {
                try {
                    List<Model.Revenue> revenues = dao.revenueByYear();
                    displayRevenues(revenues);
                }
                catch(Exception e){
                }
            } else {
                JOptionPane.showMessageDialog(frame, "No option selected");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        JFrame frame = new JFrame("Modal Dialog Example");
        // Set the size of the frame
        frame.setSize(300, 200);
        // Set the default close operation
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        
        JPanel panel = new JPanel();
        JTextField searchField = new JTextField(20);

        // Add components to the panel
        panel.add(new JLabel("Search Flight: "));
        panel.add(searchField);

        // Create a JOptionPane with the panel
        int result = JOptionPane.showConfirmDialog(frame, panel, "Search Dialog", JOptionPane.OK_CANCEL_OPTION);

        // Check if the submit button is clicked
        if (result == JOptionPane.OK_OPTION) {
            // Get the text from the search field
            String searchText = searchField.getText();
            // Perform some action with the search text (e.g., print it)
            System.out.println("Search Text: " + searchText);
            try {
                    List<Model.Revenue> revenues = dao.revenueByFlightName(searchText);
                    displayRevenues(revenues);
            }
            catch(Exception e){
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

