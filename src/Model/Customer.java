package Model;

/**
 *
 * @author Quang Nguyen
 */
public class Customer {
    private int id;
    private String name;
    private String phoneNumber;
    private String address;

    // Constructor
    public Customer(int id, String name, String phoneNumber, String address) {
        this.id = id;
        this.name = name;
        this.phoneNumber = phoneNumber;
        this.address = address;
    }

    // Getters and setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    // toString method to print customer details
    @Override
    public String toString() {
        return "Customer{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", phoneNumber='" + phoneNumber + '\'' +
                ", address='" + address + '\'' +
                '}';
    }

    // Example of how to use the Customer class
    public static void main(String[] args) {
        Customer customer1 = new Customer(1, "John Doe", "1234567890", "123 Main St");
        System.out.println(customer1);
    }
}

